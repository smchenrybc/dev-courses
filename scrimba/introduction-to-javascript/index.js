let studentAnswer = 'D';

// switch is an if/else if statement
switch(studentAnswer) {
  case 'A':
    console.log('A is wrong!');
    break;
  case 'B':
    console.log('B is wrong!');
    break;
  case 'C':
    console.log('C is correct!');
    break;
  default:
    console.log('Please answer A to C!');
}
